class Vowel{
	public static void main(String[]args)
	{
	char z='o';
	if(z=='a'||z=='e'||z=='i'||z=='o'||z=='u'){
		switch(z)
		{
		case 'a':
			System.out.println(z);
			break;
		case 'e':
			System.out.println(z);
			break;
		case 'i':
			System.out.println(z);
			break;
		case 'o':
			System.out.println(z);
			break;
		case 'u':
			System.out.println(z);
			break;
		default:
			System.out.println(z);
		}
	}
	else
	System.out.println("consonant");
}
}
		
		
		
	